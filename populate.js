// Function to parse CSV data
function parseCSVData(csvData) {
    // Split the CSV data into rows
    const rows = csvData.trim().split('\n');

    // Parse the rows into an array of objects
    const data = rows.map(row => {
        const [dateString, systolic, diastolic, weight] = row.split(',');
        const date = new Date(dateString).toISOString().split('T')[0]; // Convert date string to ISO format
        return { date, systolic: parseInt(systolic), diastolic: parseInt(diastolic), weight: parseFloat(weight) };
    });

    // Store the data in local storage
    localStorage.setItem('healthData', JSON.stringify(data));

    console.log('Data has been parsed and stored in local storage.');
}

